#Ejercico calcular el área de un rectángulo
print("Calcular el área de un rectángulo")
print("-------------------------------------")
alto=int(input("Proporciona el valor del alto de tu rectángulo:"))
ancho=int(input("Proporciona el valor del ancho de tu rectángulo:"))

perimetro=(alto+ancho)*2
area= alto*ancho

print("-------------------------------------")
print(f"El valor del perimetro es: {perimetro}")
print(f"El valor del área es: {area}")

