#Operadores de comparacion
a=4
b=6
#Operado =
resultado = (a==b)
print(f"Resultado de comparar {resultado}")

resultado= a!=b
print(f"Resultado !=: {resultado}")

#Operado >
resultado = a>b
print(f"Resultado >: {resultado}")

#operado >=
resultado= a>=b
print(f"Resultado >= {resultado}")

#operador a < b
resultado= a<b
print(f"Resultado <: {resultado}")

#operador <=
resultado= a<=b
print(f"Resultado <=: {resultado}")